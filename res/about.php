	<h1>Welcome to Flood Zone NYC</h1>
	<section>
		<p>Flood Zone NYC was born out of a frustration with the official NYC hurricane evacuation zone tool. I found that it took too many steps to find the most important information (if you were in a flood zone), and the information on what to do afterwards was a bit disjointed</p>
		<p>This tool uses an alternative color scheme for the evacuation zones which gives symbolic meaning to users, advancing from most intense to least via hue change from purple, to red, to yellow. It uses a simplified map to make the most important information, the evacuation zones, more accessible to users. And it works responsively so that you can use this tool on any touch enabled smart phone.</p>
