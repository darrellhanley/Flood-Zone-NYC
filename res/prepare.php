		<h1>Not in an evac zone</h1>
		<div>
			<section>
				<h2>Assemble an emergency supply kit</h2>
				<p>All areas of the city could face hurricane-related hazards such as high winds, flooding, tornadoes, and loss of utilities. You may be instructed to shelter in place (stay at home) for several days until the hurricane passes.</p>
			</section>
		</div>
	</section>
	<section>
		<h1>In a high rise building</h1>
		<div>
			<section>
				<p>Residents of high-rise apartment buildings may face special risks from hurricanes even if they live outside evacuation zone boundaries.</p>
				<p>If you live in a high-rise building outside an evacuation zone, be prepared to take shelter on or below the 10th floor.</p>
				<p>If you live in a high-rise building located in an evacuation zone, heed evacuation orders.</p>
			</section>
