		<h1>In an evac zone</h1>
		<div>
			<section>
				<h2>Prepare a disaster plan</h2>
				<p>Develop a plan with your household members that outlines what to do, how to find each other, and how to communicate if a hurricane strikes New York.</p>
				<p>If you rent your home, renter’s insurance will insure the items inside your apartment. If you are a homeowner, make sure your home is properly insured — flood and wind damage are not covered in a basic homeowner’s policy.</p>
			</section>
			<section>
				<h2>Know where you will go</h2>
				<p>The city strongly recommends evacuees stay with friends or family who live outside evacuation zone boundaries. For those who have no other shelter, the City will open hurricane shelters throughout the five boroughs.</p>
				<p>To ensure efficient use of resources, the City will ask all evacuees to report to an evacuation center. Once at the evacuation center, evacuees will either be assigned to a hurricane shelter in the same facility or transported to an associated hurricane shelter by bus.</p>
			</section>
			<section>
				<h2>Keep a go bag ready</h2>
				<p>Every household member should have a small Go Bag — a collection of items you may need during an evacuation packed in an easy-to-carry container such as a backpack. A Go Bag should be easily accessible if you have to leave your home in a hurry.</p>
			</section>
			<section>
				<h2>Assemble an emergency supply kit</h2>
				<p>You may be instructed to shelter in place (stay at home) during a hurricane. Keep enough supplies in your home to survive for at least three days.</p>
			</section>
		</div>
	</section>
